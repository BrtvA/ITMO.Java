package Laboratory.Lab02.Task01;

public class Calculator {
    public int summation(int a, int b) {
        return a + b;
    }

    public long summation(long a, long b) {
        return a + b;
    }

    public double summation(double a, double b) {
        return a + b;
    }


    public int subtraction(int a, int b) {
        return a - b;
    }

    public long subtraction(long a, long b) {
        return a - b;
    }

    public double subtraction(double a, double b) {
        return a - b;
    }


    public double division(int a, int b) {
        return a / (double)b;
    }

    public double division(long a, long b) {
        return a / (double)b;
    }

    public double division(double a, double b) {
        return a / b;
    }


    public int multiplication(int a, int b) {
        return a * b;
    }

    public long multiplication(long a, long b) {
        return a * b;
    }

    public double multiplication(double a, double b) {
        return a * b;
    }
}